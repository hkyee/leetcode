class solution:
    def twoSum(self, nums: List[int], target: int) -> List[int]:
        n = len(nums)
        for i in range(n - 1):
            for j in range(1 + i, n):
                if nums[j] + nums[i] == target:
                    return [i, j]

        return []  # No solution found


